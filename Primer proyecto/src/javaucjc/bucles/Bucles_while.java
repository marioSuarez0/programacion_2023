package javaucjc.bucles;

public class Bucles_while {

	public static void main(String[] args) {
		
		// While -> Evalúa y luego ejecuta
		int num = 4;
		while (num <= 4) {
			System.out.println(num);
			;
		}
		
		// Do-While -> Primero ejecuta y luego evalúa
		do {
			System.out.println(num);
		}while(num > 3);
		
		
		
	}
}
